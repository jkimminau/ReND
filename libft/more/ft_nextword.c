#include <libft.h>

char	*ft_nextword(char *str)
{
	if (!str)
		return (str);
	while (*str && (*str != ' ' && *str != '\t' && *str != '\n'))
		str++;
	while (*str && (*str == ' ' || *str == '\t' || *str == '\n'))
		str++;
	return (str);
}
